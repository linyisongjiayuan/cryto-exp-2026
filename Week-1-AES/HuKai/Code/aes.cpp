void SubByte( word8 X[16] )
{
    for ( int i = 0; i < 16; i++ )
        X[i] = Sbox[ X[i] ];
}

/*
 * 0  4  8  12 
 * 1  5  9  13
 * 2  6  10 14
 * 3  7  11 15 
*/
void ShiftRow( word8 X[16] )
{
    word8 temp[16];
    memcpy( temp, X, 16 );

    X[1] = temp[5];
    X[2] = temp[10];
    X[3] = temp[15];

    X[5] = temp[9];
    X[6] = temp[14];
    X[7] = temp[3];

    X[9] = temp[13];
    X[10] = temp[2];
    X[11] = temp[7];

    X[13] = temp[1];
    X[14] = temp[6];
    X[15] = temp[11];
}

word8 xtime( word8 x )
{
    if ( x >> 7 & 1 )
        return ( x << 1 ) ^ 0x1b;
    else
        return ( x << 1 );
}

word8 mul( word8 x, word8 y )
{
    word8 X[8] = { x };
    for ( int i = 1; i < 8; i++ )
        X[i] = xtime( X[i-1] );
    word8 res = 0;
    for ( int i = 0; i < 8; i++ )
        if ( y >> i & 1 )
            res ^= X[i];
    return res;
}


